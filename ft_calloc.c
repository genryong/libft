#include "libft.h"
#include <string.h>
#include <stdlib.h>

void	*ft_calloc(size_t count, size_t	size)
{
	void	*ptr;
	if (!count || !size)
	{
		count = 1;
		size = 1;
	}
	ptr = malloc(count * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, count * size);
	return (ptr);
}
